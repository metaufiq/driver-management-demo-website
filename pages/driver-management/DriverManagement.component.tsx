import type { NextPage } from 'next'
import Head from 'next/head'
import { useState } from 'react';
import { FaSearch } from 'react-icons/fa';
import { HiOutlinePlus } from 'react-icons/hi';

import { PAGE } from '../../constants';
import useUsers from '../../hooks/useUsers';
import useUsersQuery from '../../hooks/useUsersQuery';
import TextInput from '../../components/TextInput';
import ListDriver from '../../components/ListDriver';
import PaginationBar from '../../components/PaginationBar';
import Button from '../../components/Button';
import { 
  HeaderContainer,
  HeaderDescription, 
  HeaderTitle, 
  HeaderUtilsContainer
} from './DriverManagement.component.styles';
import { SetPageIndex, SetSearchInput } from './DriverManagement.component.types';

export const _onInputSearch = (setSearchInput: SetSearchInput)=>(event: React.ChangeEvent<HTMLInputElement>)=>{
  setSearchInput(event.target.value)
}

export const _onNextPage = (setPageIndex: SetPageIndex)=>()=>{
  setPageIndex(index=>index+1)
}

export const _onPrevPage = (setPageIndex: SetPageIndex)=>()=>{
  setPageIndex(index=>index-1)
}

const DriverManagement: NextPage = () => {
  const {users:initialValue, isLoading, isError, refetch} = useUsersQuery();
  const [pageIndex, setPageIndex] = useState(1);
  const [searchInput, setSearchInput]= useState<string>();
  const {users} = useUsers({initialValue, searchInput, pageIndex})

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <HeaderContainer>
        <div>
          <HeaderTitle>DRIVER MANAGEMENT</HeaderTitle>
          <HeaderDescription>Data Driver bekerja dengan Anda.</HeaderDescription>
        </div>
        <HeaderUtilsContainer>
          <TextInput
            placeholder='Cari Driver'
            onChange={_onInputSearch(setSearchInput)}
            type='text'
            leftIcon={<FaSearch/>}
          />
          <Button textAlign='start' buttonType='PRIMARY'>
              TAMBAH DRIVER
              <HiOutlinePlus/>
          </Button>
        </HeaderUtilsContainer>
      </HeaderContainer>

      <br/>
      <ListDriver
        data={users}
        isLoading={isLoading}
        isError={isError}
        refetch={refetch}
      />

      <PaginationBar
        onNext={_onNextPage(setPageIndex)}
        onPrev={_onPrevPage(setPageIndex)}
        pageIndex={pageIndex}
        totalPages={PAGE}
      />
    </>
  )
}

export default DriverManagement
